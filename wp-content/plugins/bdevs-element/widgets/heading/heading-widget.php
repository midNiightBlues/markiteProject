    protected function render() {

        $settings = $this->get_settings_for_display();

        $this->add_inline_editing_attributes( 'title', 'basic' );
        $this->add_render_attribute( 'title', 'class', 'section__title section__title-4 section__title-4-p-2 section-title' );

        $title = bdevs_element_kses_basic( $settings['title'] );

        ?>

        <?php if ( $settings['design_style'] === 'style_3' ):
            $this->add_inline_editing_attributes( 'title', 'basic' );
            $this->add_render_attribute( 'title', 'class', 'section__title section__title-4 section__title-4-p-2 bdevs-el-title' );

        ?>
                   
        <div class="section-heading section__title-wrapper section__title-wrapper-4 wow fadeInUp2 p-0 bdevs-el-content" data-wow-delay=".3s">
            <span class="section__pre-title-img">
                <?php if ( $settings['type'] === 'image' && ( $settings['image']['url'] || $settings['image']['id'] ) ):
                    $this->get_render_attribute_string( 'image' );
                    $settings['hover_animation'] = 'disable-animation'; // hack to prevent image hover animation
                    ?>
                    <figure>
                     <?php echo Group_Control_Image_Size::get_attachment_image_html( $settings, 'thumbnail', 'image' ); ?>
                    </figure>
                    <?php elseif ( !empty( $settings['icon'] ) || !empty( $settings['selected_icon']['value'] ) ): ?>
                    <figure>
                        <?php bdevs_element_render_icon( $settings, 'icon', 'selected_icon' );?>
                    </figure>
                <?php endif;?>
            </span>

                <?php if ( $settings['sub_title'] ): ?>
                <span class="section__pre-title purple sub-title bdevs-el-subtitle"><?php echo bdevs_element_kses_intermediate( $settings['sub_title'] ); ?></span>
                <?php endif;?>

            <?php printf( '<%1$s %2$s>%3$s</%1$s>',
                tag_escape( $settings['title_tag'] ),
                $this->get_render_attribute_string( 'title' ),
                $title
                );
            ?>
           <?php if ( $settings['description'] ): ?>
                <p><?php echo bdevs_element_kses_intermediate( $settings['description'] ); ?></p>
            <?php endif;?>
        </div>

        <?php elseif ( $settings['design_style'] === 'style_2' ):
            $this->add_inline_editing_attributes( 'title', 'basic' );
            $this->add_render_attribute( 'title', 'class', 'section__title section__title-3 section-title bdevs-el-title' );
         ?>

            <div class="section__title-wrapper section__title-wrapper-3 mb-45 wow fadeInUp2 section-heading p-0 bdevs-el-content" data-wow-delay=".3s">
                <span class="section__pre-title-img">
                    <?php if ( $settings['type'] === 'image' && ( $settings['image']['url'] || $settings['image']['id'] ) ):
                        $this->get_render_attribute_string( 'image' );
                        $settings['hover_animation'] = 'disable-animation'; // hack to prevent image hover animation
                        ?>
                        <figure>
                         <?php echo Group_Control_Image_Size::get_attachment_image_html( $settings, 'thumbnail', 'image' ); ?>
                        </figure>
                        <?php elseif ( !empty( $settings['icon'] ) || !empty( $settings['selected_icon']['value'] ) ): ?>
                        <figure>
                            <?php bdevs_element_render_icon( $settings, 'icon', 'selected_icon' );?>
                        </figure>
                    <?php endif;?>
                </span>

                <?php if ( $settings['sub_title'] ): ?>
                    <span class="section__pre-title purple sub-title bdevs-el-subtitle"><?php echo bdevs_element_kses_intermediate( $settings['sub_title'] ); ?></span>
                <?php endif;?>

                <?php printf( '<%1$s %2$s>%3$s</%1$s>',
                        tag_escape( $settings['title_tag'] ),
                        $this->get_render_attribute_string( 'title' ),
                        $title
                    );
                ?>
               <?php if ( $settings['description'] ): ?>
                    <p><?php echo bdevs_element_kses_intermediate( $settings['description'] ); ?></p>
                <?php endif;?>
            </div>

        <?php else: 
            $this->add_inline_editing_attributes( 'title', 'basic' );
            $this->add_render_attribute( 'title', 'class', 'section__title bdevs-el-title' );
        ?>

        <div class="section__title-wrapper bdevs-el-content">
            <?php printf( '<%1$s %2$s>%3$s</%1$s>',
                tag_escape( $settings['title_tag'] ),
                $this->get_render_attribute_string( 'title' ),
                $title
                );
            ?>
            <?php if ( $settings['description'] ): ?>
                <p><?php echo bdevs_element_kses_intermediate( $settings['description'] ); ?></p>
            <?php endif;?>
        </div>

    <?php endif;?>
        <?php
    }
